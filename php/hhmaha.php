                <h4 class="hhmaha-title">HH Mahavishnu Goswami</h4>
                <p class="hhmaha-p">His Holiness Srila Mahavishnu Goswami Maharaj was born in the outskirts of Mumbai in
                    1919 on the auspicious day of Dattātreya Purnima in the month of mārga-śīrṣa [November-December]. He
                    appeared in an eight generation Vaishnava family. At the tender age, he began studying the holy
                    scriptures of Srimad Bhagavat-gita and Srimad Bhagavatam in earnest. He is highly educated having
                    obtained a masters degree in both Sanskrit and English and is equally conversant in Marathi,
                    Gujarati, Hindi, Urdu and the Gujarati dialect Kachhi. His burning desire to realize God was finally
                    answered, when he met Srila Prabhupada, the founder acharya of the International Society of Krsna
                    Consciousness (ISKCON). Maharaj first took darshan of Srila Prabhupāda in 1973 when, as President of
                    the London Gujarati Community Society, he invited Prabhupäda to be guest-of-honour at the society’s
                    annual function. There Srila Prabhupada’s lecture marked the turning point in Maharaj’s life. Srila
                    Prabhupāda had stated that whenever we meet our friends, we ask them so many material things, like
                    how is your business, your family etc. But this is simply a waste of time. If we really cared for
                    them, then when we meet them, we should ask them how far they have progressed, since we last met, in
                    the matter of stopping the repetition of the cycle of birth and death. Since that profound meeting,
                    H H Mahāvishnu Goswami Maharaj has dedicated his life to the rigorous study of Śrīmad-Bhāgavatam.
                </p>
                <p class="hhmaha-p">His Holiness Srila Mahāvishnu Goswami Maharaj, is a brilliantly divine personality
                    and such kind of personalities are rarely seen on the stage of human life. Anybody who has come in
                    touch with Maharaj experienced that he was not just a spiritual master, but a sweet loving father,
                    who, by his loving dealings with everyone, made them experience the love of Kṛṣṇa. He is an
                    embodiment of all the exalted qualities. His deep serenity, the vast limitless wellspring of
                    knowledge he possessed, the beautiful sobriety he exhibited, his gentle, sweet and simple demeanor,
                    the soft expressiveness of his features and the natural flow of all his actions could capture
                    everyone’s heart.
                </p>
                <p class="hhmaha-p">Maharaj could relate even the most ordinary event to the scriptures and for even a
                    seemingly ordinary subject matter, Maharaj could offer a spiritual perspective. He was a genuine
                    guide in our struggle to carry on the material life, as well as for understanding the transcendence.
                    His presentation of the scriptures and especially Śrīmad-Bhāgavatam which is his life and soul was
                    not a dry academic subject to be theorized and armchair philosophized about. Maharaj spoke and lived
                    the essence of the scriptures and made everyone understand that the scriptures are not in the cloud
                    or in the sky but very much on the ground, intimately related to our day-to-day life. That way he
                    was a practical Maha Bhagavata and a down to earth Paramahamsa. The humility which Maharaj exhibited
                    is a conclusive proof of his greatness.
                </p>
                <p class="hhmaha-p">Maharaj is simultaneously grave and jolly and often he laughed and joked and
                    brightened the whole atmosphere with his effulgent Vaikuntha smile but even his lighter mood was
                    fully Kṛṣṇa conscious and not frivolous. Maharaj’s every movement, every word and every action had a
                    transcendental purpose in it. Throughout his whole life and at the time of leaving the world,
                    Maharaj showed us how not to be bewildered by the inevitable miseries of the material existence and
                    always remain aloof simply by remembering the lotus feet of the Lord. (saṁsāra-dharmair
                    avimuhyamānaḥ smṛtyā harer bhāgavata-pradhānaḥ – SB 11.2.49).
                </p>
                <p class="hhmaha-p">Once His Grace Viraja Kṛṣṇa Prabhuji humbly asked His Holiness Mahāvishnu Goswami
                    Maharaj how he was able to become so famous a devotee all over the world. Maharaj replied with his
                    trademark, uncommon humility that it was because he lived every moment of his life based on the
                    verse 1.6.26 from Śrīmad-Bhāgavatam. This particular verse spoken by Srila Narada Muni reveals the
                    nature of an unflinching servitor of the Supreme Lord.
                </p>
                <p class="hhmaha-p"><em>nāmāny anantasya hata-trapaḥ paṭhan<br> guhyāni bhadrāṇi kṛtāni ca smaran<br>gāṁ
                        paryaṭaṁs tuṣṭa-manā gata-spṛhaḥ<br> kālaṁ pratīkṣan vimado vimatsaraḥ</em>
                </p>
                <p class="hhmaha-p"><strong>“Thus I began chanting the holy name and fame of the Lord by repeated
                        recitation, ignoring all the formalities of the material world. Such chanting and remembering of
                        the transcendental pastimes of the Lord are benedictory. So doing, I traveled all over the
                        earth, fully satisfied, humble and unenvious.”</strong>
                </p>
                <p class="hhmaha-p">Anyone who comes in touch with His Holiness cannot go away without the impression
                    that Kṛṣṇa consciousness is all common sense, practical, logical and simple. The one shloka which
                    Maharaj had in his heart even till his winding up pastimes was SB 1.5.40, the instruction of Srila
                    Narada Muni to his disciple Srila Vyasadev on the power of the pure subject matter of the
                    transcendental pastimes of the Supreme Lord and how it is satisfying both to the general mass as
                    well as great learned men.
                </p>
                <p class="hhmaha-p"><em>tvam apy adabhra-śruta viśrutaṁ vibhoḥ<br> samāpyate yena vidāṁ
                        bubhutsitam<br>prākhyāhi duḥkhair muhur arditātmanāṁ<br> saṅkleśa-nirvāṇam uśanti nānyathā</em>
                </p>
                <p class="hhmaha-p"><strong>Please, therefore, describe the Almighty Lord’s activities which you have
                        learned by your vast knowledge of the Vedas, for that will satisfy the hankerings of great
                        learned men and at the same time mitigate the miseries of the masses of common people who are
                        always suffering from material pangs. Indeed, there is no other way to get out of such
                        miseries.</strong>
                </p>
                <p class="hhmaha-p">Maharaj urged everyone that the preaching of Śrīmad-Bhāgavatam with emphasis on its
                    practical aspect is the one and only way to mitigate the miseries of common people who are suffering
                    from the material pangs. His instructions ranged from organizing one’s own shaving kit, washing
                    one’s own clothes, to living a life without a tinge of expectation from anyone. Maharaj firmly
                    emphasized that without organizing our material lives in a perfect manner, there is no question of
                    progressing spiritually.
                </p>
                <p class="hhmaha-p">Maharaj is unflinchingly attached to the lotus feet of Srila Prabhupāda and studied
                    His Divine Grace’s books very deeply. With his compassionate, powerful, intense, pure and earnest
                    preaching and encouragement, Maharaj could impress upon the living entities to translate all the
                    instructions of scripture into real life experiences by changing the behavior for good. Maharaj is
                    of the firm belief that Srila Prabhupāda’s literatures are so powerful that by the force of this
                    literary influence, a revolution can be brought in the lives of the conditioned souls and thus save
                    them from the miserable conditions. Maharaj’s appreciation and attachment to Srila Prabhupāda’s
                    transcendental literature is so much that Maharaj says that if it had been possible, Prabhupāda
                    would have physically taken out his heart and put it in these books.
                </p>
                <p class="hhmaha-p">At about 3:25 pm on 25th January 2010, our beloved spiritual master, H H Mahävishnu
                    Goswami entered into his final pastimes in this mortal world and left us all in great grief of
                    separation at the Bhaktivedanta Hospital, Mira Road, Mumbai, India. Those who had the good fortune
                    of meeting him soon became aware that the Śrīmad-Bhāgavatam was indeed his very life and soul. In a
                    very simple and sublime manner he would provide anyone practical guidance in their lives based on
                    the teachings of the Bhāgavatam, and with this he has awakened so many souls to the importance of
                    the amala purana Śrīmad-Bhāgavatam. May he live forever by his divine instructions in the hearts of
                    his sincere followers.
                </p>
                <p class="hhmaha-p">The following verse from Srimad Bhagavatam 2.6.34 spoken by Lord Brahma to Srila
                    Narada Muni reveals to us a glimpse into the transcendental bhakti that His Holiness Mahavishnu
                    Goswami Maharaj epitomized in his life.
                </p>
                <p class="hhmaha-p"><em>na bhāratī me&nbsp;’ṅga mṛṣopalakṣyate<br> na vai kvacin me manaso mṛṣā
                        gatiḥ<br>na me hṛṣīkāṇi patanty asat-pathe<br> yan me hṛdautkaṇṭhyavatā dhṛto hariḥ</em>
                </p>
                <p class="hhmaha-p"><strong>, because I have caught hold of the lotus feet of the Supreme Personality of
                        Godhead, Hari, with great zeal, whatever I say has never proved to have been false. Nor is the
                        progress of my mind ever deterred. Nor are my senses ever degraded by temporary attachment to
                        matter.”</strong>
                </p>
                <p class="hhmaha-p">For many, Maharaj is a humble and loving Vaishnava, who has made them follow the
                    teachings of the Supreme Lord and realize the true value of human life. Still others cannot help,
                    but be completely and helplessly touched by Maharaj’s love and compassion. In today’s age His
                    Holiness’s love and humility coupled with his profound wisdom and realization, have brought many
                    fallen souls to the lotus feet of Prabhupāda and the Supreme Lord. We offer our deepest respect unto
                    His Holiness Mahävishnu Goswami’s lotus feet for kindly guiding us on the path back to the Supreme
                    One. Now more than ever, his divine instructions are the only shelter for his disciples and all
                    sincere souls seeking to reunite with the Supreme Lord Kṛṣṇa.
                </p>
